package jnetman.network;

public class DuplicateElementException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 2609100214171728880L;

	/**
	 * 
	 */

	public DuplicateElementException() {
	}

	public DuplicateElementException(String arg0) {
		super(arg0);
	}

	public DuplicateElementException(Throwable arg0) {
		super(arg0);
	}

	public DuplicateElementException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
